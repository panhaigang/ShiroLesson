package cn.et.lesson02.entity;

public class Menu {
	private String menuId;
	private String menuName;
	private String menuUrl;
	private String menuFilter;
	private String isMemnu;
	public String getMenuId() {
		return menuId;
	}
	public void setMenuId(String menuId) {
		this.menuId = menuId;
	}
	public String getMenuName() {
		return menuName;
	}
	public void setMenuName(String menuName) {
		this.menuName = menuName;
	}
	public String getMenuUrl() {
		return menuUrl;
	}
	public void setMenuUrl(String menuUrl) {
		this.menuUrl = menuUrl;
	}
	public String getMenuFilter() {
		return menuFilter;
	}
	public void setMenuFilter(String menuFilter) {
		this.menuFilter = menuFilter;
	}
	public String getIsMemnu() {
		return isMemnu;
	}
	public void setIsMemnu(String isMemnu) {
		this.isMemnu = isMemnu;
	}
	
	
	
	
	
	
}
